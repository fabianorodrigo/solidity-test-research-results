    await contractRelevantToken.methods["initialize(string,uint8,string,string,address,uint256,uint256,uint256,uint256,uint256,uint256,uint256)"]("1807y",63,"IsLibrary","IsLibrary",accounts[5],59,6,11,54,10,112,99999,{from:accounts[0]});
